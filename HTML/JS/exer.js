$('ul.back li').click(function(){
  let idx = $(this).text();                   //등운동 -> 선택한 리스트 ex)풀업
  let final_result = move_url(idx);   //리스트에 대한 리턴값을 배열로 리턴
  let M_url = final_result[0];     //배열의 첫번째 인덱스는 유튜브 url
  let Muscle = final_result[1];    //배열의 두번째 인덱스는 근육
  let stance = final_result[2];    //배열의 세번째 인덱스는 운동강도
  let pic2 = final_result[3];      //배열의 다섯번째 인덱스는 근육사진

  let url = M_url;                     
  $("a#target_url").prop("href", url);      //id == target_url 의 url 값을 선택한 index에 맞춰 변환
  let num_idx = $(this).index();            // 선택한 index값을 number로 변환
  
  eng_title[0] = find_text(num_idx, Exercise1);        //배열에서 number에 맞는 운동이름ENG를 가져옴
  work_name.innerText = idx;
  eng_title.innerText = eng_title[0];
  kinds.innerText = Muscle;
  recommend.innerText = stance;
  document.getElementById("muscle_pic").src = pic2;
});

